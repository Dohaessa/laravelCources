    {

        // dd(request());
        $course= new Course();

        $course->title =  $request->txtTitle;
        $course->description = $request->txtDescription;
        $course->completed=false;

        $image=$request->file('fimage');
        $name=$image->getClientOriginalName();
        $image-> move(public_path().'/images/', $name);



    }

   
